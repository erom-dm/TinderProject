package tinder.models;

public class Opinion {

    private int who;
    private int whom;
    private boolean opinion;

    public int getWho() {
        return who;
    }

    public void setWho(int who) {
        this.who = who;
    }

    public int getWhom() {
        return whom;
    }

    public void setWhom(int whom) {
        this.whom = whom;
    }

    public boolean isOpinion() {
        return opinion;
    }

    public void setOpinion(boolean opinion) {
        this.opinion = opinion;
    }
}
